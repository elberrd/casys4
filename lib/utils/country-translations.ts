/**
 * Translates a country name to the specified locale
 *
 * @param countryName - The country name in English
 * @param locale - The target locale (e.g., "pt", "en")
 * @returns The translated country name, or the original if no translation exists
 *
 * @example
 * translateCountryName("Turkey", "pt") // "Turquia"
 * translateCountryName("Brazil", "pt") // "Brasil"
 * translateCountryName("Brazil", "en") // "Brazil"
 */
export function translateCountryName(countryName: string | undefined | null, locale: string): string {
  if (!countryName) return "";

  // If locale is English or not Portuguese, return original name
  if (locale !== "pt") {
    return countryName;
  }

  // Return Portuguese translation if available, otherwise return original
  return countryNamesPt[countryName] || countryName;
}
